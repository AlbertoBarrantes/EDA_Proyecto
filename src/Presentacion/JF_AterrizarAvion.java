    public void llenaCB() {

        jComboBox1.removeAllItems();

        Nodo aux = insTC.retornaListaAvionesVolando2();

        while (aux != null) {
            jComboBox1.addItem("ID: " + aux.id + ",\t     Piloto: " + aux.piloto + ",\t     Modelo: " + aux.modelo);
            aux = aux.sig;
        }
    }
    
